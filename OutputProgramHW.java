public class OutputProgramHW {
    public static void main(String args[]) {
        // output program
        System.out.print("Hello World");
        System.out.print("Hello World"); // if we use print: we get output: Hello WorldHello World
        System.out.println("Hello World");
        System.out.print("Hello World"); // if we use println: we get output: Hello World (and then next in next line)
                                         // Hello World
        System.out.print("Hello World\n");
        System.out.print("Hello World"); // if we use \n also: we get output: Hello World (and then next in next line)
                                         // Hello World
        System.out.print("Hello World\n Lecture 2"); // post /n everything will print along with spaces
    }
}