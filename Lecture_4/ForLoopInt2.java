import java.util.*;

public class ForLoopInt2 {
	public static void main(String[] args) {
		// learning for loop
		// print numbers 0 to 10 using for loop
		// i++ => i= i+1
		for (int i = 0; i < 11; i++) {
			// directly print i
			System.out.print(i + " "); // output comes in one line one after the other with space in between
		}
	}
}